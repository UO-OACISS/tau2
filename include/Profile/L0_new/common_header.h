static std::string get_symbol(API_TRACING_ID id){
    constexpr size_t tracing_api_name_size = sizeof(tracing_api_name) / sizeof(tracing_api_name[0]);
    if (id == UnknownTracingId || static_cast<size_t>(id) >= tracing_api_name_size) {
      return "UnknownAPIName";
    } else if ((id > L0StartTracingId) && (id < L0EndTracingId)) {
      // L0+RT APIs: array starts at index 0
      auto index = id - L0StartTracingId - 1;
      return tracing_api_name[index];
    } else if ((id > ExtStartTracingId) && (id < ExtEndTracingId)) {
      // Extension APIs: array starts after L0+RT APIs
      constexpr auto l0_count = L0EndTracingId - L0StartTracingId - 1;
      auto index = l0_count + (id - ExtStartTracingId - 1);
      return tracing_api_name[index];
    } else if ((id >= OCLStartTracingId) && (id < OCLEndTracingId)) {
      // OCL APIs: array starts after L0+RT and Extension APIs
      constexpr auto l0_count = L0EndTracingId - L0StartTracingId - 1;
      constexpr auto ext_count = ExtEndTracingId - ExtStartTracingId - 1;
      auto index = l0_count + ext_count + (id - OCLStartTracingId);
      return tracing_api_name[index];
    } else {
      // Error: never come here
      return "UnknownAPIName";
    }
  }
